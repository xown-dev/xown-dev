		<script type="text/javascript">
			var month = new Date().getMonth();
			var fullYear = new Date().getFullYear();

			var nextMonth = new Date('April 30, 69 00:20:18');

			var datepicker = new ej.calendars.DatePicker({
				format: 'yyyy-MM-dd',
				dayHeaderFormat: "Abbreviated",
				placeholder: 'Choose a date (West Africa Time Zone)',
				allowEdit: false,
				openOnFocus: true,
				serverTimezoneOffset: 'Africa/Lagos',
				// Sets the min.
				min: new Date(fullYear, month , new Date().getDate()),
				//Sets the max.
				max: new Date(fullYear, nextMonth.getMonth(), nextMonth.getDate()),
				// Sets the value.
				value: new Date(fullYear, month , 11),
				renderDayCell: function (args) {

					var day = args.date.getDay();

					if (day == 0 || day == 1 || day == 3 || day == 6) {
						// Sets isDisabled to true to disable the date.
						args.isDisabled = true;
					}
				},
				// When the calendar has a new value...
				change: function(e) {

					// Initialize all variables
					var url = $("input[id='base_url']").val();
					absolute_url = url + "home/processdate";

					const ye = new Intl.DateTimeFormat('en', { year: 'numeric' }).format(e.value);
					const mo = new Intl.DateTimeFormat('en', { month: '2-digit' }).format(e.value);
					const da = new Intl.DateTimeFormat('en', { day: '2-digit' }).format(e.value);

					var formattedDate = `${ye}-${mo}-${da}`;

					loadAvailableSlotsBySelectedDate(e, absolute_url, formattedDate);
				},
				// If the calendar value is cleared...
				cleared: function (e) {
					removeTimeSlots(e);
				}
			});
			
			datepicker.appendTo('#date_picker_element');
		</script>

